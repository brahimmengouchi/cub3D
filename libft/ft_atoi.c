#include "libft.h"

long long	ft_atoi(const char *str)
{
	int			i;
	int			n;
	long long	nm;

	i = 0;
	n = 1;
	nm = 0;
	while (str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nm = (str[i] - 48) + (nm * 10);
		i++;
	}
	return (n * nm);
}
